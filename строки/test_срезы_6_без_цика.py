s = input()   

print(len(s))            # выводим кол-во введеных символов
print(s[:] * 3)          # выводим строку 3 раза
print(s[0])              # выводим 1 ый символ строки
print(s[:3])             # выводим первые 3 символа строки
print(s[-3:])            # выводим последние 3 символа строки
print(s[::-1])           # выводим строку в обратном порядке
print(s[1:len(s) -1])    # выводим строку с удаленым 1 и последним символом